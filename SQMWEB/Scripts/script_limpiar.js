    
 function pop_borrar1()
    {
      var a1 = document.getElementById(TextBox1).value;
      var a2 = document.getElementById(TextBox2).value;
      var update = document.getElementById(btnactualiza).disabled;
      if (  trim(a1).length != '0'  && trim(a2).length  != '0'  &&  update == false )  
      { document.getElementById(TextBox1).value=''; 
        document.getElementById(TextBox2).value=''; 
        document.getElementById(txt21).value='0.000'; } 
    } ;
          
